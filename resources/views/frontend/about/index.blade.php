Cách viết trang giới thiệu hay

1. Trang web giới thiệu doanh nghiệp đẹp, hấp dẫn là gì?
Website là một bộ mặt của doanh nghiệp, cũng như là một trang thông tin với mục đích là để giới thiệu, cập nhật những thông tin về các doanh nghiệp, sản phẩm, hoạt động cũng như tin tức, chia sẻ bí quyết,… để phát triển thương hiệu, tồn tại trên Internet như một tổ chức có điều hành.

Khi có trang web của doanh nghiệp, thì trong trang web đó cần có trang giới thiệu về doanh nghiệp của mình để khách hàng tìm hiểu về DN dễ dàng hơn. Trang giới thiệu (About) là loại trang thường thấy nhất trong mỗi website. Một lời giới thiệu hay có giá trị rất "đắt" trong việc thu hút khách hàng, độc giả, thậm chí là tạo niềm tin, uy tín cho website, chủ cửa hàng, công ty, doanh nghiệp.   Vì vậy bạn nên đầu tư thời gian chăm chút cho trang giới thiệu doanh nghiệp để có thể viết một trang giới thiệu hợp lý và hiệu quả.

2. Lợi ích trang giới thiệu về doanh nghiệp.
Lợi ích của trang giới thiệu

Ưu điểm của trang giới thiệu

Trên thực tế, khi thiết kế website doanh nghiệp thì trang giới thiệu hay mang lại nhiều giá trị tích cực lên website và doanh nghiệp, cả những lợi ích ngay trước mắt lẫn lâu dài. Nó có những lợi ích như:

- Giới thiệu thông tin dễ dàng tới khách hàng.
Thực tế thì thay vì đưa danh thiếp hay giới thiệu cho khách hàng, bạn có thể mời họ truy cập vào trang web giới thiệu của công ty một cách tiện lợi và nhanh chóng. Tất nhiên thông tin trên webiste còn đầy đủ và chi tiết hơn rất nhiều so với danh thiếp.

- Có được niềm tin từ khách hàng.
Nếu khách hàng truy cập vào trang giới thiệu của doanh nghiệp thì chắc chắc họ đang muốn tìm hiểm về website cũng như thông tin về doanh nghiệp. Việc các doanh nghiệp cần phải làm là viết lời giới thiệu công ty tốt sẽ khiến người dùng tin tưởng vào công ty và những giá trị mà họ có thể nhận được.

- Tạo hứng thú, dẫn dắt khách hàng.
Trang giới thiệu hấp dẫn sẽ tạo được sự hứng thú của khách hàng tới sản phẩm và dịch vụ của công ty. Cộng thêm việc họ đã tin tưởng vào uy tín của website, tỉ lệ chuyển đổi cũng sẽ tăng lên phần nào.

3. Giới thiệu một số cách viết trang giới thiệu hay cho website.
3.1. Kể câu chuyện về bản thân, doanh nghiệp.
kể một câu chuyện về doanh nghiệp

kể về câu chuyện cảm động của doanh nghiệp

Hãy kể một câu chuyện liên quan tới doanh nghiệp để có thể làm “Rung động” được người đọc. Câu chuyện có thể là quá trình phát triển vượt qua khó khăn của công ty, những thử thách, thất bại trong quá khứ cho thấy công ty bạn bản lĩnh và kiên cường đến nhường nào hay người khởi xướng công ty từ khi khởi nghiệp,… Những câu chuyện như vậy giúp khách hàng hiểu thêm về công ty của bạn phần nào cũng như tin tưởng doanh nghiệp của bạn thêm nhiều hơn.

3.2. Nội dung viết lời giới thiệu hay
Nội dung trong phần lời giới thiệu cần thân thiện, cuốn hút. Cách viết bạn nên sử dụng văn phong tự nhiên hơn, gần gũi hơn để tạo cái nhìn thân thiện với khách hàng. Hãy tránh sử dụng các biệt ngữ, thuật ngữ quá phức tạp trong ngành.

Khi khách hàng đọc trang giới thiệu này, thì cách viết thân mật sẽ làm họ cảm thấy thân thiện và chân thành hơn. Vì thế, hãy giới thiệu website với một thái độ thân thiện và cởi mở hơn, thậm chí nếu bạn là một doanh nghiệp hướng đến khách hàng ở lứa tuổi teen cũng có thể mang chút hài hước, vui nhộn.

3.3. Giới thiệu về nhân lực của doanh nghiệp.
giới thiệu về nhân lực công ty

giới thiệu về nhân lực công ty

Đây cũng là một cách hay để giới thiệu về công ty, việc minh bạch thông tin nhân viên, đưa lên website cũng góp phần làm cho doanh nghiệp trở nên đáng tin cậy.  

Bạn có thể ghi ra thông tin của các thành viên chính trong công ty, sở thích thú vị hoặc sở trường và sở đoản của họ. Bạn cũng có thể viết ra một dòng mô tả ngắn, nhận xét của bạn về từng người hay lý do mọi người sẽ thích làm việc chung với thành viên đó.

Những việc này cho khách hàng thấy rằng môi trường làm việc của bạn vừa chuyên nghiệp như một công ty, vừa ấm cúng như một gia đình vậy.

3.4. Viết bài giới thiệu chuẩn SEO.
Để viết một bài giới thiệu trên website chuẩn SEO bạn cần phải có những nội dung sau đây:

- Trang giới thiệu web cần có 1 thẻ Heading 1 (mỗi trang duy nhất 1 H1) nên đặt tiêu đề là H1 để tối ưu SEO nhất.

- Nội dung cần chứa các từ khóa liên quan đến dịch vụ của bạn cần cung cấp, k nên nhắc quá nhiều lần (một bài chỉ cần chứa khoảng 3% từ khóa)

- Hình ảnh/video sắc nét phù hợp với bài viết

- Bài viết cần tách thành các ý nhỏ, có bố cục sắp xếp logic hiệu quả.

